/**
 * Download multiple papers comparison in different formats
 */
export function downloadComparisonAsText(papers: { fileName: string; summary: string }[]): void {
  const content = papers
    .map((paper) => `\n${'='.repeat(60)}\n📄 ${paper.fileName}\n${'='.repeat(60)}\n\n${paper.summary}`)
    .join('\n\n');

  const element = document.createElement('a');
  const file = new Blob([content], { type: 'text/plain' });
  element.href = URL.createObjectURL(file);
  element.download = 'papers_comparison.txt';
  document.body.appendChild(element);
  element.click();
  document.body.removeChild(element);
}

