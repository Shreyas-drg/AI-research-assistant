import React from 'react';

interface DownloadFormats {
  type: 'txt' | 'md' | 'json' | 'csv' | 'html';
}

/**
 * Download summary in TXT format
 */
export function downloadAsText(summary: string, fileName: string): void {
  const element = document.createElement('a');
  const file = new Blob([summary], { type: 'text/plain' });
  element.href = URL.createObjectURL(file);
  element.download = `${fileName.replace('.pdf', '')}_summary.txt`;
  document.body.appendChild(element);
  element.click();
  document.body.removeChild(element);
}

/**
 * Download summary in Markdown format
 */
export function downloadAsMarkdown(summary: string, fileName: string): void {
  const mdContent = `# Summary: ${fileName.replace('.pdf', '')}\n\n${summary}\n\n---\n*Generated by AI Research Paper Summarizer*`;
  const element = document.createElement('a');
  const file = new Blob([mdContent], { type: 'text/markdown' });
  element.href = URL.createObjectURL(file);
  element.download = `${fileName.replace('.pdf', '')}_summary.md`;
  document.body.appendChild(element);
  element.click();
  document.body.removeChild(element);
}

/**
 * Download summary in JSON format
 */
export function downloadAsJSON(summary: string, fileName: string): void {
  const jsonData = {
    fileName: fileName,
    timestamp: new Date().toISOString(),
    summary: summary,
    sections: summary.split('\n\n').map((section, idx) => ({
      id: idx + 1,
      content: section.trim(),
    })),
  };
  const element = document.createElement('a');
  const file = new Blob([JSON.stringify(jsonData, null, 2)], { type: 'application/json' });
  element.href = URL.createObjectURL(file);
  element.download = `${fileName.replace('.pdf', '')}_summary.json`;
  document.body.appendChild(element);
  element.click();
  document.body.removeChild(element);
}

/**
 * Download summary in CSV format
 */
export function downloadAsCSV(summary: string, fileName: string): void {
  const sections = summary.split('\n\n').map((section, idx) => ({
    id: idx + 1,
    content: section.trim().replace(/"/g, '""'), // Escape quotes
  }));

  const csvHeader = 'SectionID,Content\n';
  const csvRows = sections.map((s) => `${s.id},"${s.content}"`).join('\n');
  const csvContent = csvHeader + csvRows;

  const element = document.createElement('a');
  const file = new Blob([csvContent], { type: 'text/csv' });
  element.href = URL.createObjectURL(file);
  element.download = `${fileName.replace('.pdf', '')}_summary.csv`;
  document.body.appendChild(element);
  element.click();
  document.body.removeChild(element);
}

